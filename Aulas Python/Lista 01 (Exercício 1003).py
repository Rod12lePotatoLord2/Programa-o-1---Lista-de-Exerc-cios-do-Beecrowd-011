# Leia dois valores inteiros, no caso para variáveis A e B. A seguir, calcule a soma entre elas e atribua à variável SOMA. A seguir escrever o valor desta variável.

# Entrada
# O arquivo de entrada contém 2 valores inteiros.

# Saída
# Imprima a mensagem "SOMA" com todas as letras maiúsculas, com um espaço em branco antes e depois da igualdade seguido pelo valor correspondente à soma de A e B. Como todos os problemas, não esqueça de imprimir o fim de linha após o resultado, caso contrário, você receberá "Presentation Error".

A = int(input()) # A é um valor inteiro (Número inteiro)
B = int(input()) # B é um valor inteiro (Número inteiro)
                 # O Comando input serve para que a pessoa digite um valor.
SOMA = A + B # A variável SOMA foi definida para calcular a soma entre os valores inteiros de A e B, representada pelo símbolo de adição (+)

print(f"SOMA = {SOMA}")  # Print irá imprimir a mensagem.

